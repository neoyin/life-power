import 'package:shared_preferences/shared_preferences.dart';

class LocaleService {
  static LocaleService? _instance;
  static String _currentLocale = 'en';

  LocaleService._();

  static Future<LocaleService> getInstance() async {
    if (_instance == null) {
      _instance = LocaleService._();
      await _instance!._loadLocale();
    }
    return _instance!;
  }

  Future<void> _loadLocale() async {
    final prefs = await SharedPreferences.getInstance();
    _currentLocale = prefs.getString('locale') ?? 'en';
  }

  static void setLocale(String locale) {
    _currentLocale = locale;
  }

  static String get currentLocale => _currentLocale;
}

String tr(String key) {
  return _translate(key, LocaleService.currentLocale);
}

